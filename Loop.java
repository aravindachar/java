public class Loop{
    public static void main(String[] args) {
        // for loop 
        for(int i = 0 ; i < 6 ; i++){
            System.out.println("hello aravinda");
        }
        // while loop - run when you dont know how many times we should run the code
        int number = 1 ;
        while (number < 4) {
            System.out.println("while aravinda");
            number++;
        }

        // do while 
        do{
            System.out.println("do some new shit");
        }while(number < 3);
    }
}
